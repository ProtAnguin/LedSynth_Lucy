void plotEaster(String in, int key, uint8_t h, uint8_t w) {
  uint8_t cc = 0; // current column
  uint8_t cl = 0; // current line

  for (cl = 0; cl < h; cl++) { // for each line
    for (cc = 0; cc < w; cc++) { // for each column
      Serial.print(in[ cc + (cl * w) ]);
    }
    Serial.println("");
  }
}


